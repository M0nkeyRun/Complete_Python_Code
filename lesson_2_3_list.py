# List Datatype

values = [39, True, None, "Fenil", 54.30]

print(values)

# append - adds a value to the end of the list
values.append("apple")
values.append(30)
values.append(False)
values.append(["a", "b", "c"])

# copy - it creates and returns shallow copy of your list
copyValues = values.copy()

print(copyValues)
